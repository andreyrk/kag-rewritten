namespace KAG.Runtime.Types
{
    public class KVector
    {
        public float x = 0;
        public float y = 0;
        public float z = 0;

        public KVector(float x, float y, float z = 0)
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
